/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */

struct ListNode
{
    int val;
    ListNode *next;

    ListNode(int x) : val(x), next(NULL)
    {}
};

class Solution
{
        // 1->2->3->4->5->NULL
    public:
        ListNode *reverseList(ListNode *head)
        {
            // O(N)

            ListNode *cur = head;
            ListNode *prev = nullptr;
            ListNode *temp = nullptr;

            while(cur)
            {
                temp = cur->next;
                cur->next = prev;
                prev = cur;
                cur = temp;
            }

            return prev;
        }
};
