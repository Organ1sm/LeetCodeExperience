/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
struct TreeNode
{
    int val;
    TreeNode *left;
    TreeNode *right;

    TreeNode(int x) : val(x), left(NULL), right(NULL)
    {}
};

class Solution
{
    public:
        int k;
        int ans;

        int kthLargest(TreeNode *root, int k)
        {
            this->k = k;
            Dfs(root);
            return this->ans;
        }

        void Dfs(TreeNode *node)
        {
            if (node == nullptr)
            { return; }

            Dfs(node->right);
            if (this->k == 0)
            { return; }
            if (--this->k == 0)
            { this->ans = node->val; }

            Dfs(node->left);
        }
};
