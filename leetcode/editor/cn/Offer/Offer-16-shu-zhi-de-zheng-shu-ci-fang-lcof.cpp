class Solution
{
    public:
        // äºŒåˆ†æ³• é€’å½’log(n)
        double myPow(double x, int n)
        {
            if (x == 0.0 || x == 1 || n == 1)
            { return x; }


            double ans = 1.0;
            long b = n;
            if (b < 0)
            {
                x = 1 / x;
                b = -b;
            }
            while(b > 0)
            {
                if ((b & 1) == 1)
                { ans *= x; }

                x *= x;
                b = b >> 1;
            }

            return ans;
        }
};
