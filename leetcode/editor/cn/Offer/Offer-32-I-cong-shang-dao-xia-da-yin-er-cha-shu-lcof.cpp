/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */

struct TreeNode
{
    int val;
    TreeNode *left;
    TreeNode *right;

    TreeNode(int x) : val(x), left(NULL), right(NULL)
    {}
};

class Solution
{
    public:
        vector<int> levelOrder(TreeNode *root)
        {
            if (root == nullptr)
            { return {}; }

            queue<TreeNode *> q;
            q.push(root);
            vector<int> ans;

            while(!q.empty())
            {
                TreeNode *cur = q.front();
                q.pop();
                ans.push_back(cur->val);

                if (cur->left)
                { q.push(cur->left); }
                if (cur->right)
                { q.push(cur->right); }
            }

            return ans;
        }
};
