#include <iostream>
#include <vector>
#include <unordered_map>

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution
{
    public:
        char firstUniqChar(string s)
        {
            vector<char> keys;
            std::unordered_map<char, bool> hashMap;

            for (char &i : s)
            {
                if (hashMap.find(i) == hashMap.end())
                {
                    keys.push_back(i);
                    hashMap[i] = true;
                }
                else
                {
                    hashMap[i] = false;
                }
            }
            for (char &c : keys)
            {
                if (hashMap[c])
                { return c; }
            }

            return ' ';
        }
};
